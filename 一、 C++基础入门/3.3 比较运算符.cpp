#include<iostream>
using namespace std;

int main33() {
//作用：用于表达式的比较，并返回一个真值或假值
//比较运算符有以下符号

int a = 10;
int b = 20;

// == 等于
cout << (a == b) << endl;

// != 不等于
cout << (a != b) << endl;

// > 大于
cout << (a > b) << endl;

// < 小于
cout << (a < b) << endl;

// >= 大于等于
cout << (a >= b) << endl;

// <= 小于等于
cout << (a <= b) << endl;

system("pause");

return 0;
}